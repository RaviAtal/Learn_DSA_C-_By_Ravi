// Q7. How much space does the following data types take?
// int
// bool
// float
#include <iostream>
using namespace std;

int main()
{
    int a = 5;
    cout << "Space take by int :" << sizeof(a) << endl;
    bool b = 0;
    cout << "Space take by int :" << sizeof(b) << endl;
    float c = 5;
    cout << "Space take by int :" << sizeof(c) << endl;
}